/*
- check whether input letter is upper case or lower case vowel (a, A, e, E, i, I, o, O, u, U)
- return result
*/

function isVowel(char) {
  return (char === 'a' || char === 'A' || char === 'e' || char === 'E' || char === 'i' || char === 'I' || char === 'o' || char === 'O' || char === 'u' || char === 'U');
}
